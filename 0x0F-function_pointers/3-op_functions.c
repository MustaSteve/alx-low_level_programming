#include "3-calc.h"
/**
 * op_mul - mul two numbers
 * @a: 1 number
 * @b: 2 number
 *
 * Return: sum  a & b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_add - add 2 number
 * @a: 1 num
 * @b: 2 num
 *
 * Return: sum a & b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub 2 num
 * @a: 1 num
 * @b: 2 num
 *
 * Return: sum a & b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_div - Return 2 num
 * @a: 1 num
 * @b: 2 num
 *
 * Return: sum  a & b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Return 2 num
 * @a: 1 num
 * @b: 2 num
 *
 * Return: sum  a & b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
